package entities;

import core.abstracts.Entity;

public class User implements Entity{

	private static boolean defisActivated=false;
	private int id;
	private String email;
	private String password;
	private String name;
	private String lastName;
	private boolean isActivated;
	public User() {}
	public User(int id, String email, String password, String name, String lastName) {
		super();
		this.id = id;
		this.email = email;
		this.password = password;
		this.name = name;
		this.lastName = lastName;
		this.isActivated=defisActivated;
	}
	public User(int id, String email, String password, String name, String lastName,boolean isActivated) {
		super();
		this.id = id;
		this.email = email;
		this.password = password;
		this.name = name;
		this.lastName = lastName;
		this.isActivated=isActivated;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public Boolean getIsActivated() {
		return isActivated;
	}
	public void setIsActivated(Boolean isActivated) {
		this.isActivated = isActivated;
	}

}
